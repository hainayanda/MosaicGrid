import Foundation

/// Defines the alignment options for flow layout in mosaic grids.
public enum FlowMosaicAlignment {
    /// Align items to the leading edge.
    case leading
    /// Center-align items.
    case center
    /// Align items to the trailing edge.
    case trailing
}
